export const getLanguage = () => localStorage.getItem(LANGUAGE_KEY) || "English";

export const setLanguage = (language) => {
  const nextLanguage = languages[language] ? language : "English";
  localStorage.setItem(LANGUAGE_KEY, nextLanguage);
  return nextLanguage;
};

export const t = (key, replacements = {}) => {
  const language = languages[getLanguage()] || languages.English;
  const value = language.ui[key] || language.labels[key] || languages.English.ui[key] || languages.English.labels[key] || key;

  return Object.entries(replacements).reduce(
    (text, [name, replacement]) => text.replaceAll(`{${name}}`, String(replacement)),
    value
  );
};

const translateTextNode = (element) => {
  if (!element.dataset.i18nSource) {
    element.dataset.i18nSource = element.dataset.i18n || element.textContent.trim();
  }

  const source = element.dataset.i18n || element.dataset.i18nSource;
  const translated = t(source);

  if (translated !== source || element.dataset.i18n) {
    element.textContent = translated;
  }
};

export const applyTranslations = (root = document) => {
  const selectedLanguage = languages[getLanguage()] || languages.English;
  document.documentElement.lang = selectedLanguage.code;

  root.querySelectorAll("[data-i18n]").forEach(translateTextNode);
  root.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    element.setAttribute("placeholder", t(element.dataset.i18nPlaceholder));
  });

  root.querySelectorAll("h1, h2, h3, p, span, label, button, a, strong").forEach((element) => {
    if (element.children.length || element.dataset.i18n === "") return;
    translateTextNode(element);
  });
};

document.addEventListener("DOMContentLoaded", () => applyTranslations());
